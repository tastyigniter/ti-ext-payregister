<h5>To obtain your Live API credentials.</h5>
<ul class="list-group list-group-flush rounded">
    <li class="list-group-item">From your PayPal business account.</li>
    <li class="list-group-item">
        You can obtain an API Access credential in one of two ways:
        <ol>
            <li>Select API Access from the Activity menu at the top of the screen,</li>
            <li>or from Account preferences > API Access.</li>
        </ol>
    </li>
    <li class="list-group-item">Scroll down and choose Manage API credentials under NVP/SOAP API Integration (Classic).</li>
    <li class="list-group-item">If you haven't previously, create keys. Otherwise, duplicate the API Username, API Password, and Signature.</li>
    <li class="list-group-item">Paste the API Username, API Password, and Signature into the plugin's corresponding fields.</li>
</ul>
